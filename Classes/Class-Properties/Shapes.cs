namespace BuildingFormulas
{
    using System;

    public class Shapes
    {
        /// <summary>
        /// The circle.
        /// </summary>
        private const string Circle = "circle";

        /// <summary>
        /// The cone.
        /// </summary>
        private const string Cone = "cone";

        /// <summary>
        /// The cylinder.
        /// </summary>
        private const string Cylinder = "cylinder";

        /// <summary>
        /// The hexagon.
        /// </summary>
        private const string Hexagon = "hexagon";

